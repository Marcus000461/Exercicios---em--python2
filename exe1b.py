# --- Programa Simples de Boas-Vindas ---

print("Olá! Por favor, responda a algumas perguntas para continuarmos.")

# 1. Entrada de Dados (Perguntar e armazenar as respostas)
nome = input("Qual é o seu nome completo? ")
idade = input("Qual é a sua idade? ") # A idade será lida como texto (string)
endereco = input("Qual é o seu endereço (Rua, Número, Bairro, Cidade)? ")

# 2. Saída dos Dados Coletados (Opcional, mas bom para confirmar)
# O "\n" serve para pular uma linha antes de imprimir o texto.
print("\n--- Suas Informações ---")
print(f"Nome: {nome}")
print(f"Idade: {idade} anos") # Adicionamos "anos" para clareza
print(f"Endereço: {endereco}")

